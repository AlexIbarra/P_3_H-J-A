package carta;

public class Combo {
	
	private Carta carta1;
	private Carta carta2;
	
	
	public Combo(Carta cartaa, Carta cartab)
	{
		carta1 = cartaa;
		carta2 = cartab;
	}
	
	
	public Carta getCarta1() {
		return carta1;
	}

	public Carta getCarta2() {
		return carta2;
	}

	
	
	
}
